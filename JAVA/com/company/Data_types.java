package com.company;

public class Data_types {
    public static void main(String[] args) {
        byte age= 15;
        short a= 12;
        int b= 202;
        float F= 12.6F;
        long L= 1299999L;
        double d= 33;
        char ch= 'a';
        boolean e = true;
        System.out.print("The value of age is: ");
        System.out.println(age);
        System.out.print("The value of a is: ");
        System.out.println(a);
        System.out.print("The value of b is: ");
        System.out.println(b);
        System.out.print("The value of F is: ");
        System.out.println(F);
        System.out.print("The value of L is: ");
        System.out.println(L);
        System.out.print("The value of d is: ");
        System.out.println(d);
        System.out.print("The value of ch is: ");
        System.out.println(ch);
        System.out.print("The value of e is: ");
        System.out.println(e);


    }
}
